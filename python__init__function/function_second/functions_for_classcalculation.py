class FunctionsForClassFirst:


	def __init__(self, variable1, variable2):
		self.variable1 = variable1
		self.variable2 = variable2
	
	def addition(self):
		addition = self.variable1 + self.variable2
		return addition

	def subtraction(self):
		subtraction = self.variable1 - self.variable2
		return subtraction

	def multiplication(self):
		multiplication = self.variable1 * self.variable2
		return multiplication
	
	def division(self):
		division = self.variable1 / self.variable2
		return division
		